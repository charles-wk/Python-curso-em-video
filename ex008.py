#Transformando m em km, hm, dam, dm, cm, mm.
valorm = float(input('Digite o valor em metro: '))
print('O valor em de {}m em km é igual a: {}km'.format(valorm, valorm/1000))
print('O valor em de {}m em hm é igual a: {}hm'.format(valorm, valorm/100))
print('O valor em de {}m em dam é igual a: {}dam'.format(valorm, valorm/10))
print('O valor em de {}m em dm é igual a: {}dm'.format(valorm, valorm*10))
print('O valor de {}m é igual a: {}cm'.format(valorm, valorm*100))
print('O valor de {}m é igual a: {}mm'.format(valorm, valorm*1000))

